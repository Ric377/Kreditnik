package com.kreditnik.app.data

/**
 * Определяет типы долговых обязательств, доступные в приложении.
 *
 * @property displayName Человекочитаемое название типа, используемое в UI.
 */
enum class LoanType(val displayName: String) {
    /** Стандартный потребительский или иной кредит. */
    CREDIT("Кредит"),

    /** Кредитная карта с льготным периодом. */
    CARD("Кредитная карта"),

    /** Беспроцентная рассрочка. */
    INSTALLMENT("Рассрочка"),

    /** Частный долг физическому лицу. */
    DEBT("Долг")
}