package com.kreditnik.app.data

/**
 * Определяет соглашение о подсчете дней для расчета процентов.
 * @deprecated Используется для обратной совместимости. Новая логика
 * управляется полем [Loan.usesSberbankCalculation].
 */
enum class DayCountConvention {
    /** Метод, использующий фактическое количество дней в году (365/366). */
    SBER,

    /** Метод, предполагающий 30 дней в месяце и 360 в году. */
    RETAIL
}